# fahrenheit to celsius

fahrenheit_c=float(input("enter the celsius value="))

celsius_cal=(fahrenheit_c-32)/1.8

print("%0.1f degree fahrenheit to %0.1f degree celsius"%(fahrenheit_c,celsius_cal))

